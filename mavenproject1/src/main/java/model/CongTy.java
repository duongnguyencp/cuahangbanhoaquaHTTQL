package model;

/**
 *
 * @author Duong
 */
public class CongTy {
    private int id;
    private String tenCongTy;
    private String diaChi;

    public CongTy() {
    }

    public CongTy(int id, String tenCongTy, String diaChi) {
        this.id = id;
        this.tenCongTy = tenCongTy;
        this.diaChi = diaChi;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenCongTy() {
        return tenCongTy;
    }

    public void setTenCongTy(String tenCongTy) {
        this.tenCongTy = tenCongTy;
    }

    public String getDiaChi() {
        return diaChi;
    }

    public void setDiaChi(String diaChi) {
        this.diaChi = diaChi;
    }
    
}
